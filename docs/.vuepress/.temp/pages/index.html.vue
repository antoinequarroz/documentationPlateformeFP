<h2 id="📋-objectifs-du-projet" tabindex="-1"><a class="header-anchor" href="#📋-objectifs-du-projet"><span>📋 Objectifs du projet</span></a></h2>
<p>Le but principal de ce projet est de développer une <strong>plateforme en ligne</strong> qui permet aux utilisateurs de s'inscrire, de gérer des données en temps réel, et d'interagir avec des composants UI dynamiques. En utilisant les technologies ci-dessus, le projet vise à :</p>
<ul>
<li>Fournir une <strong>interface utilisateur fluide</strong> et interactive, grâce à Vue.js et PrimeVue.</li>
<li>Gérer des données <strong>en temps réel</strong> et de manière sécurisée avec Firebase.</li>
<li>Permettre une gestion centralisée des utilisateurs et des données pour les administrateurs du système.</li>
</ul>
